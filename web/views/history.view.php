        <h1>Asociación de Comerciantes, Hosteleros y Empresas de Servicios de Vitoria</h1>
        
        <p><strong>COMERCIO VITORIA</strong> nace de la iniciativa de los comerciantes de la calle Gorbea, junto a Euskaldendak (federación de comerciantes del Pais Vasco) y otras asociaciones de barrio y gremiales de Vitoria-Gasteiz y Araba.</p>

        <p>La asociación de la Calle Gorbea, como conocerán, es el referente del comercio tradicional vitoriano. Muchas actividades y acciones en estos años nos avalan. Desde nuestra modesta opinión creemos que podemos servir de colaboración y consejo a la hora de toma de decisiones de asuntos comerciales.</p>

        <h2>¿POR QUÉ NACE COMERCIO VITORIA?</h2>
        <p>En Vitoria-Gasteiz vemos un déficit muy grande del asociacionismo comercial y un mal planteamiento constante por la plataforma que actualmente se erige como “teórico representante” del comercio vitoriano.</p>

        <p>Es un asunto que viene desde hace tiempo. Las asociaciones de barrio y zonales han sido absorbidas y maniatadas, perdiendo margen de maniobra y personalidad propia. El flujo de la dinamización se ha concentrado en el centro urbano, y la gran mayoría de las acciones son turísticas-hosteleras. Vemos que hay una carencia de organización en asuntos que afectan al / la comerciante de a pie de calle de Vitoria-Gasteiz.</p>

        <p>Como necesidad de recobrar los barrios y comercios propios de cada zona vemos necesario un cambio de estrategia por parte del ayuntamiento, al que proponemos dar la vuelta a esta situación.</p>

        <p><strong>COMERCIO VITORIA</strong> es una agrupación de asociaciones comerciales zonales y de barrios formada para aglutinar un verdadero diálogo sobre la problemática actual de las tiendas de Vitoria-Gasteiz.</p>

        <p>Un órgano de coordinación real en la que las asociaciones y pequeños comerciantes de la ciudad tienen voz, voto y personalidad propia para adecuarse a la realidad de su barrio. También muy importante es la defensa de los intereses de los pequeños negocios y la interlocución con instituciones.</p>

        <h2>¿QUÉ PLANTEAMOS?</h2>
        <p>El reto de la Agrupación es intervenir en los foros adecuados y colaborar con las instituciones para lograr que la voz del comercio Vitoriano se tenga en cuenta a la hora de tomar decisiones relacionadas con el sector.</p>

        <p>Servir de barómetro comercial eficaz para proponer soluciones a la problemática de los establecimientos de nuestra ciudad.</p>

        <p>Una voz que atienda a las necesidades de barrios en cuanto a su día a día.</p>

        <p>Por tanto, una creación de foros e interlocutores en el que pueda participar cualquier persona que tenga un negocio en la ciudad.</p>

        <p>No tendrá cuota para no suponer un coste a los comercios que pueden encontrarse en problemas de viabilidad.</p>

        <h2>OBJETIVOS</h2>
        <p>La Agrupación surge con unos OBJETIVOS muy definidos, que son los que se detallan a continuación:</p>

        <ul>
            <li>La representación, defensa, promoción y difusión de los intereses económicos y profesionales de todos sus miembros.</li>
            <li>El desarrollo de todo lo concerniente al marketing urbano e imagen del comercio vitoriano.</li>
            <li>El estudio, desarrollo y ejecución de planes de urbanismo comercial.</li>
            <li>Recoger de primera mano a través de las asociaciones las necesidades de los clientes de los comercios urbanos y trabajar para que sus expectativas se vean cumplidas.</li>
            <li>Trabajar para conseguir que la ciudadanía perciba la importancia de realizar el consumo en Vitoria-Gasteiz, fomentando la sostenibilidad medioambiental, económica y social, así como el reparto de la riqueza.</li>
            <li>Transmitir la imagen de calidad del comercio urbano.</li>
            <li>Trabajar a favor de la modernización del sector, con herramientas que faciliten la gestión, animando y apoyando con las actuaciones necesarias a los comercios punteros y concienciando al resto de que el camino a seguir es ser más competitivo día a día.</li>
        </ul>

