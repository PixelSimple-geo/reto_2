        <p>La influencia de una actuación urbanística en una calle conlleva directamente una reacción en las calles de alrededor. Queremos que cuando se quiera llevar a cabo una de estas intervenciones por parte del ayuntamiento, se constituya un “consejo de afectados/as” donde las asociaciones de comercio y vecinos tengan voz y voto; ya que nosotros/as somos los que mejor conocemos la situación real de la zona, más allá de planos/mapas con demasiada visión arquitectónica en papel.</p>

        <p>Un ejemplo lo tenemos en la calle Coronación donde existe incertidumbre sobre el diseño/eliminación de zonas de aparcamiento. Nos gustaría conocer el proyecto y que se nos consultara (así como a otros agentes) para poder aconsejar y participar en un diseño que sin duda afectará a los clientes existentes y/o nuevos.</p>

